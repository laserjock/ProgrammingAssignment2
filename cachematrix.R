## These functions are used to create cacheable inverse matrices.
## First, a list of functions is created to set and get a cachable matrix as
## well as the inverse of the matrix.
## Second, the inverse itself is found using solve().

## makeCacheMatrix creates a list of functions for a cacheable inverse matrix

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) m <<- inverse
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


## cacheSolve gives the inverse of a matrix, caching if applicable

cacheSolve <- function(x, ...) {
  m <- x$getinverse()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinverse(m)
  m
}